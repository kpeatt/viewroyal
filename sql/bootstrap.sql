  query_embedding halfvec(384),
  match_threshold float,
  match_count int,
  filter_bylaw_id bigint DEFAULT NULL
)
RETURNS TABLE (
  id bigint,
  bylaw_id bigint,
  chunk_index int,
  text_content text,
  similarity float
)
LANGUAGE plpgsql
SET search_path = 'public'
AS $$
BEGIN
  RETURN QUERY
  SELECT
    bc.id,
    bc.bylaw_id,
    bc.chunk_index,
    bc.text_content,
    1 - (bc.embedding <=> query_embedding) AS similarity
  FROM bylaw_chunks bc
  WHERE (filter_bylaw_id IS NULL OR bc.bylaw_id = filter_bylaw_id)
    AND 1 - (bc.embedding <=> query_embedding) > match_threshold
  ORDER BY bc.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;

CREATE OR REPLACE FUNCTION match_key_statements (
  query_embedding halfvec(384),
  match_threshold float,
  match_count int
)
RETURNS TABLE (
  id bigint,
  meeting_id bigint,
  agenda_item_id bigint,
  speaker_name text,
  statement_type text,
  statement_text text,
  context text,
  similarity float
)
LANGUAGE plpgsql
SET search_path = 'public'
AS $$
BEGIN
  RETURN QUERY
  SELECT
    ks.id, ks.meeting_id, ks.agenda_item_id, ks.speaker_name,
    ks.statement_type, ks.statement_text, ks.context,
    1 - (ks.embedding <=> query_embedding) AS similarity
  FROM key_statements ks
  WHERE 1 - (ks.embedding <=> query_embedding) > match_threshold
  ORDER BY ks.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;

